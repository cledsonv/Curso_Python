n = input('digite algo: ')
print(f'seu tipo{type(n)}')
print(f'ele é numero {n.isnumeric()}')
print(f'ele é alfabetico {n.isalpha()}')
print(f'ele é alfanumerico {n.isalnum()}')
print(f'ele é decimal {n.isdecimal()}')
print(f'ele é digito {n.isdigit()}')
print(f'ele é espaço {n.isspace()}')
print(f'ele é tudo maiusculo {n.isupper()}')
print(f'ele é menusculo {n.islower()}')
print(f'ele esta capitalizada {n.istitle()}')


